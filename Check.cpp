int Check::check(char L_IA[4], char L_NA[4], char R_NA[4], char R_IA[4]) 
{
    Counter(L_IA, L_NA, R_NA, R_IA);
    if (((Count_LN != 0) && Count_LI > Count_LN) || (Count_RN != 0) && Count_RI > Count_RN) 
    {
        system("CLS");
        game_over(); Sleep(1300);   
        lost();      Sleep(1600);
        return 1;
    }
    else if ((Count_LI == 3 && Count_LN == 3) && (Count_RI == 0 && Count_RN == 0)) 
    {
        system("CLS");
        game_over();  Sleep(1300);  
        won();        Sleep(1600);
        return 2;
    }
    else
        return 0;
}
